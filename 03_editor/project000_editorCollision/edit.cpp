//============================================================
//	生成処理
//============================================================
CEdit *CEdit::Create(const EMode mode)
{
	// エディットの生成
	CEdit *pEdit = nullptr;
	switch (mode)
	{ // モードごとの処理
	case MODE_PLAY:
		pEdit = new CEditPlay;
		break;

	case MODE_COLL_SET:
		pEdit = new CEditColl;
		break;

	default:
		assert(false);
		break;
	}

	if (pEdit == nullptr)
	{ // 生成していない場合

		return nullptr;
	}
	else
	{ // 生成している場合

		// エディットの初期化
		if (FAILED(pEdit->Init()))
		{ // 初期化に失敗した場合

			// エディットの破棄
			SAFE_DELETE(pEdit);
			return nullptr;
		}

		// 確保したアドレスを返す
		return pEdit;
	}
}

//============================================================
//	破棄処理
//============================================================
void CEdit::Release(CEdit *&prEdit)
{
	// エディットの終了
	assert(prEdit != nullptr);
	prEdit->Uninit();

	// メモリ開放
	SAFE_DELETE(prEdit);
}
